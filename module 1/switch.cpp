#include<bits/stdc++.h>
using namespace std;
int main(){

    int day;
    cin>>day;

    switch (day)
    {
    case /* constant-expression */1:
        /* code */
        cout<<"saturday"<<endl;
        break;
    case 2:
        cout<<"sunday"<<endl;
        break;
    case 3:
        cout<<"Monday"<<endl;
        break;
    case 4:
        cout<<"Tuesday"<<endl;
        break;
    case 5:
        cout<<"Wednesday"<<endl;
        break;
    case 6:
        cout<<"Thursday"<<endl;
        break;
    case 7:
        cout<<"Friday"<<endl;
        break;
    default:
        cout<<"Unknown"<<endl;
        break;
    }
    
  return 0;
}